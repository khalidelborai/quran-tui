#!/usr/bin/env bash
set -euo pipefail

VERSION="${1:?version is required}"
PLATFORM_RAW="${2:?platform is required}"
PLATFORM="$(printf '%s' "$PLATFORM_RAW" | tr '[:upper:]' '[:lower:]')"
PROJECT_ROOT="$(pwd)"
APP_NAME="quran-tui"
BINARY_PATH="$PROJECT_ROOT/target/release/$APP_NAME"
DIST_DIR="$PROJECT_ROOT/dist"
PACKAGE_NAME="$APP_NAME-$VERSION-$PLATFORM"
STAGE_DIR="$DIST_DIR/$PACKAGE_NAME"
ARCHIVE_PATH="$DIST_DIR/$PACKAGE_NAME.tar.gz"
WRAPPER_SOURCE="$PROJECT_ROOT/packaging/linux/$APP_NAME"
WRAPPER_PATH="$STAGE_DIR/$APP_NAME"
COMPAT_LAUNCHER_PATH="$STAGE_DIR/run-quran-tui.sh"
PAYLOAD_PATH="$STAGE_DIR/$APP_NAME-bin"

rm -rf "$STAGE_DIR" "$ARCHIVE_PATH"
mkdir -p "$STAGE_DIR"
cp "$BINARY_PATH" "$PAYLOAD_PATH"
cp "$PROJECT_ROOT/README.md" "$STAGE_DIR/README.md"
cp "$PROJECT_ROOT/SETUP.md" "$STAGE_DIR/SETUP.md"
cp "$WRAPPER_SOURCE" "$WRAPPER_PATH"
cp "$WRAPPER_SOURCE" "$COMPAT_LAUNCHER_PATH"
chmod +x "$WRAPPER_PATH" "$COMPAT_LAUNCHER_PATH" "$PAYLOAD_PATH"
tar -czf "$ARCHIVE_PATH" -C "$DIST_DIR" "$PACKAGE_NAME"
rm -rf "$STAGE_DIR"

echo "Created $ARCHIVE_PATH"
